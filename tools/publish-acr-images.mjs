#!/usr/bin/env node

import { mkdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { spawnSync } from "node:child_process";

import { printHelp, projectEnv, repoRoot, run } from "./shared.mjs";

if (process.argv.includes("--help") || process.argv.includes("-h")) {
  printHelp(
    "publish-acr-images.mjs",
    "Build MemoTree runtime images, tag them for Alibaba Cloud ACR, and push them.",
    [
      "Required env:",
      "  ACR_REGISTRY    Example: registry.cn-hangzhou.aliyuncs.com",
      "  ACR_NAMESPACE   Example: memotree",
      "Optional env:",
      "  IMAGE_TAG       Defaults to current git short SHA",
      "  DOCKER_CONFIG   Defaults to .docker-config in the repository",
      "Output:",
      "  deploy/releases/staging-current.env",
    ],
  );
  process.exit(0);
}

const registry = requiredEnv("ACR_REGISTRY").replace(/\/+$/, "");
const namespace = requiredEnv("ACR_NAMESPACE").replace(/^\/+|\/+$/g, "");
const releaseCommit = gitShortSHA();
const imageTag = process.env.IMAGE_TAG || releaseCommit;
const releaseCreatedAt = new Date().toISOString();

const dockerConfig = process.env.DOCKER_CONFIG || path.join(repoRoot, ".docker-config");
mkdirSync(dockerConfig, { recursive: true });
const releasesDir = path.join(repoRoot, "deploy", "releases");
const releaseEnvPath = path.join(releasesDir, "staging-current.env");

const dockerEnv = { DOCKER_CONFIG: dockerConfig };
const env = projectEnv(dockerEnv);

const images = [
  { local: "memotree-api:local", remote: `${registry}/${namespace}/memotree-api:${imageTag}`, dockerfile: "deploy/api.Dockerfile" },
  { local: "memotree-worker:local", remote: `${registry}/${namespace}/memotree-worker:${imageTag}`, dockerfile: "deploy/worker.Dockerfile" },
  { local: "memotree-web:local", remote: `${registry}/${namespace}/memotree-web:${imageTag}`, dockerfile: "deploy/web.Dockerfile" },
];

for (const image of images) {
  run("docker", ["build", "-f", image.dockerfile, "-t", image.local, "-t", image.remote, "."], { cwd: repoRoot, env });
  run("docker", ["push", image.remote], { cwd: repoRoot, env });
}

const releaseEnv = [
  "# Generated by tools/publish-acr-images.mjs. Safe to copy to the staging server.",
  `API_IMAGE=${images[0].remote}`,
  `WORKER_IMAGE=${images[1].remote}`,
  `WEB_IMAGE=${images[2].remote}`,
  `RELEASE_COMMIT=${releaseCommit}`,
  `RELEASE_IMAGE_TAG=${imageTag}`,
  `RELEASE_CREATED_AT=${releaseCreatedAt}`,
  "",
].join("\n");

assertReleaseEnvHasNoSecrets(releaseEnv);
mkdirSync(releasesDir, { recursive: true });
writeFileSync(releaseEnvPath, releaseEnv, "utf8");

console.log(`\nImages pushed. Release env written to ${path.relative(repoRoot, releaseEnvPath)}:\n`);
console.log(releaseEnv);
console.log("Copy this file to the staging server and run deploy/staging-deploy.sh with it.");

function requiredEnv(name) {
  const value = process.env[name];
  if (!value) {
    throw new Error(`${name} is required. Run with --help for examples.`);
  }
  return value;
}

function gitShortSHA() {
  const result = spawnSync("git", ["rev-parse", "--short=12", "HEAD"], {
    cwd: repoRoot,
    encoding: "utf8",
  });
  if (result.status === 0) {
    return result.stdout.trim();
  }
  return "manual";
}

function assertReleaseEnvHasNoSecrets(content) {
  // 发布版本文件只负责传递镜像版本；长期密钥仍留在服务器的 deploy/.env.staging。
  const forbidden = [
    "PASSWORD",
    "SECRET",
    "ACCESS_KEY",
    "MYSQL_DSN",
    "SESSION",
    "BUCKET",
  ];
  for (const key of forbidden) {
    if (content.includes(key)) {
      throw new Error(`Release env unexpectedly contains secret-like key: ${key}`);
    }
  }
}
